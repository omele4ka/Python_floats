print('Задача 6. Ход конём')


# В рамках разработки шахматного ИИ стоит новая задача.
# По заданным вещественным координатам коня
# и второй точки программа должна определить может ли конь ходить в эту точку.
# 
# Используйте как можно меньше конструкций if и логических операторов.
# Обеспечьте контроль ввода.

# Введите местоположение коня:
# 0.071
# 0.118
# Введите местоположение точки на доске:
# 0.213
# 0.068
# Конь в клетке (0, 1). Точка в клетке (2, 0).
# Да, конь может ходить в эту точку.


print('Введите местоположение коня:')
try:
    x_horse = float(input())
    y_horse = float(input())
except ValueError:
    print("Ошибка: Введите корректные вещественные числа.")
    exit(1)

print('Введите местоположение точки на доске:')
try:
    x_dot = float(input())
    y_dot = float(input())
except ValueError:
    print("Ошибка: Введите корректные вещественные числа.")
    exit(1)

if not (0 <= x_horse <= 0.8 and 0 <= y_horse <= 0.8 and 0 <= x_dot <= 0.8 and 0 <= y_dot <= 0.8):
    print("Ошибка: Координаты должны быть в диапазоне от 0 до 0.8 включительно.")
    exit(1)

x_horse = int(x_horse * 10)
y_horse = int(y_horse * 10)
x_dot = int(x_dot * 10)
y_dot = int(y_dot * 10)

dx = abs(x_horse - x_dot)
dy = abs(y_horse - y_dot)

print(f'Конь в клетке ({x_horse}, {y_horse}). Точка в клетке ({x_dot}, {y_dot}).')

if (dx == 2 and dy == 1) or (dx == 1 and dy == 2):
    print('Да, конь может ходить в эту точку.')
else:
    print('Нет, конь не может ходить в эту точку.')